int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;

	i = 0;
	while ((s1) && (s2))
	{
		if ((s1[i] == s2[i]) && (i != '\0'))
			i++;
	}
	return (s1[i] - s2[i]);
}
